//if/else statements


/*
result >= 70 - A
result >= 60 - B 
result >= 50 - C 
result >= 40 - D
result < 40 - Fail
*/

let result = 24;

if(result >= 70){
	console.log("Grade: A");
}else if(result >= 60){
	console.log("Grade: B");
}else if(result >= 50){
	console.log("Grade: C");
}else if(result >= 40){
	console.log("Grade: D");
}else{
	console.log("Grade: Fail");
}

