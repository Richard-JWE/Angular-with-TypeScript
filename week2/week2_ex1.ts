let x: number = 1;

if(x == 1){
    console.log("Value assigned to variable is equal to 1");
}

if(x > 1){
    console.log("Value assigned to variable is greater than 1");
}

if(x >= 1){
    console.log("Value assigned to variable is greater than or equal to 1");
}

if(x < 1){
    console.log("Value assigned to variable is less than 1");
}

if(x <= 1){
    console.log("Value assigned to variable is less than or equal to 1");
}

if(x != 1){
    console.log("Value assigned to variable is not equal to 1");
}